#include <math.h>
#include "fdf.h"

t_mat	mat_product(t_mat *a, t_mat *b)
{
	t_mat c;

	c.a = a->a * b->a;
	c.b = 0;
	c.c = 0;
	c.d = 0;
	c.e = 0;
	c.f = 0;
	c.g = 0;
	c.h = 0;
	c.i = 0;
	c.j = 0;
	c.k = 0;
	c.l = 0;
	c.m = 0;
	c.n = 0;
	c.o = 0;
	c.p = 0;
	return (c);
}
